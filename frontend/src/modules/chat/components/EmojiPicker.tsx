export function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState<string>(Object.keys(EMOJI_CATEGORIES)[0]);

  return (
    <div className="absolute bottom-full mb-2 left-0 right-0 bg-white rounded-lg shadow-lg border border-gray-300 overflow-hidden z-50"
         style={{ width: '320px', height: '280px' }}>
      {/* Header */}
      <div className="flex items-center justify-between px-3 py-2 border-b border-gray-200 bg-gray-50">
        <div className="flex gap-1 overflow-x-auto">
          {Object.keys(EMOJI_CATEGORIES).map((category) => (
            <button
              key={category}
              onClick={() => setSelectedCategory(category)}
              className={`
                px-2 py-1 rounded text-xs font-medium transition-colors whitespace-nowrap
                ${selectedCategory === category 
                  ? 'bg-white text-blue-600' 
                  : 'text-gray-600 hover:bg-gray-100'
                }
              `}
            >
              {category.split(' ')[0]}
            </button>
          ))}
        </div>
        <button
          onClick={onClose}
          className="p-1 hover:bg-gray-200 rounded transition-colors"
        >
          <X className="w-4 h-4 text-gray-600" />
        </button>
      </div>

      {/* Emoji Grid */}
      <div className="overflow-y-auto p-3" style={{ height: '220px' }}>
        <div className="grid grid-cols-8 gap-1">
          {EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES]?.map((emoji, index) => (
            <button
              key={index}
              onClick={() => onSelect(emoji)}
              className="
                w-8 h-8 flex items-center justify-center text-xl rounded hover:bg-gray-100
                transition-colors cursor-pointer
              "
              title={emoji}
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}

